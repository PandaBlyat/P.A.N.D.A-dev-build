function formatTemplate(text: string, params?: TemplateParams): string {
  if (!params) return text;
  return text.replace(/\{(\w+)\}/g, (_match: string, name: string) => {
    const value = params[name];
    return value == null ? `{${name}}` : String(value);
  });
}

export function t(key: I18nKey, params?: TemplateParams): string {
  const language = store.get().uiLanguage;
  const value = STRINGS[language][key];
  if (value != null) return formatTemplate(value, params);

  const isDev = !!import.meta.env?.DEV;
  if (isDev && language === 'ru') return `⛔MISSING_RU:${key}`;
  if (isDev) return `⛔MISSING:${key}`;
  return key;
}

