const GAME_ITEM_LOOKUP = new Map(GAME_ITEM_CATALOG.map((item) => [item.sectionId, item] as const));

export function findGameItem(sectionId: string): GameItemCatalogEntry | undefined {
  return GAME_ITEM_LOOKUP.get(sectionId);
}

export function formatGameItemLabel(sectionId: string): string {
  if (!sectionId) return "";
  const item = findGameItem(sectionId);
  if (!item?.displayName) return sectionId;
  return `${item.displayName} (${item.sectionId})`;
}

export function getGameItemSearchText(item: GameItemCatalogEntry): string {
  return `${item.sectionId} ${item.displayName ?? ""}`.toLowerCase();
}
